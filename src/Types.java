public class Types {

    public static void main(String[] args) {
        // Primitive types
        // 1. Integer types
        byte b = 100; // 1 B, -128..127
        short s = 10000; // 2 B
        int i = 1000000000; // 4 B, max ~ 2 000 000 000
        long l = 1000000000000000000L; // 8 B

        // 2. Floating point
        float f = 10.5f;
        double d = 10.5;

        // 3. Char
        char c = 'a';

        // 4. Logical
        boolean bool = true;

        // Object types
        String str = "Java";
    }
}
